// ── Return type resolution ──

function resolveReturn(
  distFunc: string,
  returnType: string,
  distance0: number,
  distance1: number,
  closestHash: number,
): number {
  // Apply sqrt only for Euclidean distance + non-CellValue returns
  if (distFunc === "Euclidean" && returnType !== "CellValue") {
    distance0 = Math.sqrt(distance0);
    if (returnType !== "Distance") {
      distance1 = Math.sqrt(distance1);
    }
  }

  switch (returnType) {
    case "CellValue":    return closestHash * 4.656613e-10;
    case "Distance2":    return distance1 - 1.0;
    case "Distance2Add": return (distance1 + distance0) * 0.5 - 1.0;
    case "Distance2Sub": return distance1 - distance0 - 1.0;
    case "Distance2Mul": return distance1 * distance0 * 0.5 - 1.0;
    case "Distance2Div": return distance0 / distance1 - 1.0;
    case "Distance":
    default:             return distance0 - 1.0;
  }
}

// ── 2D Cellular Noise ──

export function voronoiNoise2D(
  _rng: () => number,
  cellType: string = "Euclidean",
  jitter: number = 1.0,
  returnType: string = "Distance",
  distanceFunction: string = "Euclidean",
  seed: number = 0,
): (x: number, y: number) => number {
  const cellularJitter = 0.5 * jitter;

  return (x: number, y: number) => {
    const xr = fastRound(x);
    const yr = fastRound(y);
    let distance0 = 1e30;
    let distance1 = 1e30;
    let closestHash = 0;

    let xPrimed = Math.imul(xr - 1, PRIME_X);
    const yPrimedBase = Math.imul(yr - 1, PRIME_Y);

    // Use cellType as returnType fallback for backwards compat
    const rt = returnType || cellType || "Distance";
    const df = distanceFunction || "Euclidean";

    for (let xi = xr - 1; xi <= xr + 1; xi++) {
      let yPrimed = yPrimedBase;
      for (let yi = yr - 1; yi <= yr + 1; yi++) {
        const hash = fnlHash2(seed, xPrimed, yPrimed);
        const idx = hash & 510;
        const vecX = (xi - x) + RandVecs2D[idx] * cellularJitter;
        const vecY = (yi - y) + RandVecs2D[idx | 1] * cellularJitter;

        let newDistance: number;
        switch (df) {
          case "Manhattan":
            newDistance = Math.abs(vecX) + Math.abs(vecY);
            break;
          case "Hybrid":
            newDistance = Math.abs(vecX) + Math.abs(vecY) + (vecX * vecX + vecY * vecY);
            break;
          case "Euclidean":
          case "EuclideanSq":
          default:
            newDistance = vecX * vecX + vecY * vecY;
            break;
        }

        // FNL tracking: distance1 = max(min(distance1, newDist), distance0)
        distance1 = Math.max(Math.min(distance1, newDistance), distance0);
        if (newDistance < distance0) {
          distance0 = newDistance;
          closestHash = hash;
        }

        yPrimed = (yPrimed + PRIME_Y) | 0;
      }
      xPrimed = (xPrimed + PRIME_X) | 0;
    }

    return resolveReturn(df, rt, distance0, distance1, closestHash);
  };
}

// ── 3D Cellular Noise ──

export function voronoiNoise3D(
  _rng: () => number,
  cellType: string = "Euclidean",
  jitter: number = 1.0,
  returnType: string = "Distance",
  distanceFunction: string = "Euclidean",
  seed: number = 0,
): (x: number, y: number, z: number) => number {
  const cellularJitter = 0.5 * jitter;

  return (x: number, y: number, z: number) => {
    const xr = fastRound(x);
    const yr = fastRound(y);
    const zr = fastRound(z);
    let distance0 = 1e30;
    let distance1 = 1e30;
    let closestHash = 0;

    let xPrimed = Math.imul(xr - 1, PRIME_X);
    const yPrimedBase = Math.imul(yr - 1, PRIME_Y);
    const zPrimedBase = Math.imul(zr - 1, PRIME_Z);

    const rt = returnType || cellType || "Distance";
    const df = distanceFunction || "Euclidean";

    for (let xi = xr - 1; xi <= xr + 1; xi++) {
      let yPrimed = yPrimedBase;
      for (let yi = yr - 1; yi <= yr + 1; yi++) {
        let zPrimed = zPrimedBase;
        for (let zi = zr - 1; zi <= zr + 1; zi++) {
          const hash = fnlHash3(seed, xPrimed, yPrimed, zPrimed);
          const idx = hash & 1020;
          const vecX = (xi - x) + randVecs3D[idx] * cellularJitter;
          const vecY = (yi - y) + randVecs3D[idx | 1] * cellularJitter;
          const vecZ = (zi - z) + randVecs3D[idx | 2] * cellularJitter;

          let newDistance: number;
          switch (df) {
            case "Manhattan":
              newDistance = Math.abs(vecX) + Math.abs(vecY) + Math.abs(vecZ);
              break;
            case "Hybrid":
              newDistance = Math.abs(vecX) + Math.abs(vecY) + Math.abs(vecZ) + (vecX * vecX + vecY * vecY + vecZ * vecZ);
              break;
            case "Euclidean":
            case "EuclideanSq":
            default:
              newDistance = vecX * vecX + vecY * vecY + vecZ * vecZ;
              break;
          }

          // Track 1st and 2nd nearest (same as FNL's approach)
          if (newDistance < distance1) {
            if (newDistance < distance0) {
              distance1 = distance0;
              distance0 = newDistance;
              closestHash = hash;
            } else {
              distance1 = newDistance;
            }
          }

          zPrimed = (zPrimed + PRIME_Z) | 0;
        }
        yPrimed = (yPrimed + PRIME_Y) | 0;
      }
      xPrimed = (xPrimed + PRIME_X) | 0;
    }

    return resolveReturn(df, rt, distance0, distance1, closestHash);
  };
}
