const HASH_MUL = 668265261;

function fastRound(x: number): number {
  return x >= 0 ? (x + 0.5) | 0 : (x - 0.5) | 0;
}

function fnlHash2(seed: number, xPrimed: number, yPrimed: number): number {
  let h = seed ^ xPrimed ^ yPrimed;
  h = Math.imul(h, HASH_MUL);
  return h;
}

function fnlHash3(seed: number, xPrimed: number, yPrimed: number, zPrimed: number): number {
  let h = seed ^ xPrimed ^ yPrimed ^ zPrimed;
  h = Math.imul(h, HASH_MUL);
  return h;
}

